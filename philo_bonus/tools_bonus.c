#include "philo_bonus.h"

int	ft_atoi(char *str)
{
	int					i;
	int					sign;
	unsigned long long	n;

	i = 0;
	n = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || (str[i] == '+'))
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = (n * 10) + (str[i] - 48);
		if (n > 9223372036854775807 && sign == -1)
			return (0);
		if (n > 9223372036854775807 && sign == 1)
			return (-1);
		i++;
	}
	return ((int)n * sign);
}

int	*ft_memset(int *b, int c, size_t len)
{
	int			*str;
	size_t		i;

	str = b;
	i = 0;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (b);
}
